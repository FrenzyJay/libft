#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int num)
{
	if (num < 0)
	{
		num = num * -1;
		write(1, "-", 1);
	}

	if (num > 9)
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
	else
	{
		num = num + 48;
		write(1, &num, 1);
	}
}

