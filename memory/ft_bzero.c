#include <string.h>

void	ft_bzero(void *s, size_t len)
{
	size_t	ktr;

	if (s)
	{
		ktr = 0;
		while (ktr < len)
		{
			*(char*) (s + ktr) = 0;
			ktr = ktr + 1;
		}
	}
}

